    public void startRelogio() {
        final SimpleDateFormat HHmmss = new SimpleDateFormat("HH:mm:ss");
        TimerTask task = new TimerTask() {
            @Override
            public void run() {
                horaJTF.setText(HHmmss.format(new Date()));
            }
        };
        new Timer().scheduleAtFixedRate(task, 0, 1000);
        try {
            wait();
        }
        catch (Exception ex)
        {}
    }
    
